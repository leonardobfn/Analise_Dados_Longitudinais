


Dados: proteína do leite de 79 vacas australianas.

Coleta: semanal, até 19 semanas após o parto mais precoce.

Dietas: três grupos — cevada, cevada + tremoço, apenas tremoço.

Problema: cerca de metade das séries ficam incompletas porque os partos não ocorreram todos na mesma data.

Objetivo: investigar como a dieta influencia o teor de proteína no leite, levando em conta a estrutura longitudinal e o problema de dados faltantes dependentes do tempo.